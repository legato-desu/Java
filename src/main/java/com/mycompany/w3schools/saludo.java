package com.mycompany.w3schools;

public class saludo {

    public static void main(String[] args) {

        /*
         * Dentro del main() método, podemos usar el println() método para imprimir una
         * línea de texto en la pantalla:
         */

        System.out.println("Hello World!");

        /*
         * Puedes agregar tantos println() métodos como quieras. Tenga en cuenta que
         * agregará una nueva línea para cada método:
         */

        System.out.println("Hello World!");
        System.out.println("I am learning Java.");
        System.out.println("It is awesome!");

        /*
         * También hay un print() método, que es similar a println().
         * La única diferencia es que no inserta una nueva línea al final de la salida:
         */

        System.out.print("Hello World! ");
        System.out.print("I will print on the same line.");
    }
}

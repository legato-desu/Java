package com.mycompany.w3schools.introduccion;

public class numeros {
    public static void main(String[] args) {

        /*
         * También puedes usar el println() método a imprimir números.
         * Sin embargo, a diferencia del texto, no ponemos números dentro del doble comillas:
         */
        System.out.println(3);
        System.out.println(358);
        System.out.println(50000);

        // También puede realizar cálculos matemáticos dentro del println() método:
        System.out.println(3 + 3);
        System.out.println(2 * 5);

    }

}

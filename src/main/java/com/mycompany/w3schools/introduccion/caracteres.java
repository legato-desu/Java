package com.mycompany.w3schools.introduccion;

public class caracteres {

    public static void main(String[] args) {

        /*
         * El char el tipo de datos se utiliza para almacenar un único carácter. El
         * personaje debe ser rodeado de citas individuales, como 'A' o 'c':
         */

        char miGrado = 'B';
        System.out.println(miGrado);

        /*
         * Alternativamente, si está familiarizado con los valores ASCII, puede usarlos
         * para mostrar ciertos caracteres:
         */

        char miVar1 = 65, miVar2 = 66, miVar3 = 67;
        System.out.println(miVar1);
        System.out.println(miVar2);
        System.out.println(miVar3);

        /*
         * 
         * El String el tipo de datos se utiliza para almacenar una secuencia de
         * caracteres (texto). Los valores de cadena deben estar rodeados de comillas
         * dobles:
         */

        String saludo = "Hola Mundo";
        System.out.println(saludo);

    }

}

package com.mycompany.w3schools.introduccion;

public class datosBooleanos {

    public static void main(String[] args) {

        /*
         * Muy a menudo en la programación, necesitará un tipo de datos que solo puede
         * tener uno de dos valores, como:
         * 
         * SÍ/NO
         * ENCENDIDO/APAGADO
         * VERDADERO / FALSO
         * Para esto, Java tiene un boolean tipo de datos, que solo puede tomar los
         * valores true o false:
         */

        boolean javaEsDivertido = true;
        boolean ElPescadoRico = false;
        System.out.println(javaEsDivertido); // Salida true
        System.out.println(ElPescadoRico); // Salida false
    }

}
